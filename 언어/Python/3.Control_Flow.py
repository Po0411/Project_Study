#이제 저희는 반복문을 배울 겁니다.
#반복문은 일정 조건이 참(True)이 될 때까지 실행 하는 코드 입니다.@
#이렇게 말로 설명 하는 것보다 바러 코드로 볼까요?

#아래 코드는 1부터 10까지의 숫자를 순서대로 출력하는 간단한 파이썬 코드입니다. 
